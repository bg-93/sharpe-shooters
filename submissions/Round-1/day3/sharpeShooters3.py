# Global volatility guard.  Ratios are based on the median stock return vol.
VOL_RECENT = 20
VOL_BASE = 100

_pair_pos = np.zeros(len(PAIRS), dtype=np.int8)
_pair_pending = np.zeros(len(PAIRS), dtype=np.int8)
_pair_confirm = np.zeros(len(PAIRS), dtype=np.int8)
_pair_cooldown = np.zeros(len(PAIRS), dtype=np.int16)
_pair_hold = np.zeros(len(PAIRS), dtype=np.int16)

_ll_W = None
_ll_mu = None
_ll_sd = None
_ll_resid_sd = None
_ll_last_fit = -1

_prev_nt = -1


def reset_state():
    global _pair_pos, _pair_pending, _pair_confirm, _pair_cooldown, _pair_hold
    global _ll_W, _ll_mu, _ll_sd, _ll_resid_sd, _ll_last_fit
    global _prev_nt

    _pair_pos = np.zeros(len(PAIRS), dtype=np.int8)
    _pair_pending = np.zeros(len(PAIRS), dtype=np.int8)
    _pair_confirm = np.zeros(len(PAIRS), dtype=np.int8)
    _pair_cooldown = np.zeros(len(PAIRS), dtype=np.int16)
    _pair_hold = np.zeros(len(PAIRS), dtype=np.int16)

    _ll_W = None
    _ll_mu = None
    _ll_sd = None
    _ll_resid_sd = None
    _ll_last_fit = -1

    _prev_nt = -1


def _safe_solve(a, b):
    """Solve a ridge system, with a pseudo-inverse fallback."""
    try:
        return np.linalg.solve(a, b)
    except np.linalg.LinAlgError:
        return np.linalg.pinv(a) @ b


def _market_vol_ratio(log_prices):
    """Recent market volatility divided by a slower baseline."""
    r = np.diff(log_prices[1:], axis=1)
    if r.shape[1] < VOL_BASE + VOL_RECENT:
        return 1.0

    recent = np.std(r[:, -VOL_RECENT:], axis=1)
    base = np.std(r[:, -(VOL_BASE + VOL_RECENT):-VOL_RECENT], axis=1)
    ratios = recent / np.maximum(base, 1e-8)
    ratios = ratios[np.isfinite(ratios)]
    if ratios.size == 0:
        return 1.0
    return float(np.median(ratios))


def _risk_multipliers(vol_ratio):
    """Capital cuts used only when the market enters a volatility shock."""
    if vol_ratio >= 2.00:
        return 0.10, 0.20      # lead-lag, MLR
    if vol_ratio >= 1.65:
        return 0.30, 0.45
    if vol_ratio >= 1.35:
        return 0.60, 0.75
    return 1.00, 1.00


def _pair_target_dollars(log_prices):
    target = np.zeros(N_INST)
    nt = log_prices.shape[1]
    if nt <= PAIR_ROLL + 1:
        return target

    for k, (i, j, gamma) in enumerate(PAIRS):
        spread = log_prices[i] - gamma * log_prices[j]
        history = spread[-PAIR_ROLL - 1:-1]
        spread_sd = np.std(history)
        if spread_sd <= EPS:
            continue

        z = float((spread[-1] - np.mean(history)) / spread_sd)

        ds = np.diff(spread)
        if ds.size >= PAIR_VOL_BASE + PAIR_VOL_RECENT:
            recent_vol = np.std(ds[-PAIR_VOL_RECENT:])
            base_vol = np.std(
                ds[-(PAIR_VOL_BASE + PAIR_VOL_RECENT):-PAIR_VOL_RECENT]
            )
            vol_ratio = float(recent_vol / max(base_vol, 1e-8))
        else:
            vol_ratio = 1.0

        if _pair_cooldown[k] > 0:
            _pair_cooldown[k] -= 1

        pos = int(_pair_pos[k])

        if pos == 0:
            _pair_hold[k] = 0

            # Demand a larger displacement when spread volatility is elevated.
            dynamic_entry = PAIR_ENTRY + 0.35 * max(vol_ratio - 1.0, 0.0)
            candidate = 0
            if _pair_cooldown[k] == 0 and vol_ratio < PAIR_VOL_BLOCK:
                if z >= dynamic_entry:
                    candidate = -1
                elif z <= -dynamic_entry:
                    candidate = 1

            if candidate == 0:
                _pair_pending[k] = 0
                _pair_confirm[k] = 0
            elif candidate == _pair_pending[k]:
                _pair_confirm[k] += 1
            else:
                _pair_pending[k] = candidate
                _pair_confirm[k] = 1

            if _pair_confirm[k] >= PAIR_CONFIRM_DAYS:
                pos = candidate
                _pair_pos[k] = pos
                _pair_pending[k] = 0
                _pair_confirm[k] = 0
                _pair_hold[k] = 1

        else:
            _pair_hold[k] += 1

            # A long spread entered at negative z worsens when pos*z is very
            # negative; the same expression handles a short spread.
            divergence_stop = (pos * z) <= -PAIR_STOP_Z
            volatility_stop = vol_ratio >= PAIR_VOL_BLOCK
            reverted = (
                (pos == 1 and z >= -PAIR_EXIT)
                or (pos == -1 and z <= PAIR_EXIT)
            )

            if divergence_stop or volatility_stop or reverted:
                pos = 0
                _pair_pos[k] = 0
                _pair_pending[k] = 0
                _pair_confirm[k] = 0
                _pair_hold[k] = 0
                _pair_cooldown[k] = PAIR_COOLDOWN_DAYS

        if pos != 0:
            # Keep most pair capital in ordinary regimes, but trim it when the
            # spread itself becomes unusually volatile.
            leg_scale = float(np.clip(1.15 - 0.25 * vol_ratio, 0.55, 1.0))
            leg = PAIR_LEG * leg_scale
            target[i] += pos * leg
            target[j] -= pos * gamma * leg

    return target


def _leadlag_target_dollars(prc_so_far, risk_mult):
    global _ll_W, _ll_mu, _ll_sd, _ll_resid_sd, _ll_last_fit

    nt = prc_so_far.shape[1]
    if nt < LL_MIN_HIST:
        return np.zeros(N_INST)

    r = np.diff(np.log(np.maximum(prc_so_far, EPS)), axis=1)

    if _ll_W is None or (nt - _ll_last_fit) >= LL_RETRAIN:
        x = r[:, :-1].T
        y = r[:, 1:].T

        _ll_mu = np.mean(x, axis=0)
        _ll_sd = np.std(x, axis=0)
        _ll_sd = np.where(_ll_sd > 1e-12, _ll_sd, 1.0)
        xs = (x - _ll_mu) / _ll_sd

        ridge = xs.T @ xs + LL_LAM * np.eye(N_INST)
        _ll_W = _safe_solve(ridge, xs.T @ y)

        residual = y - xs @ _ll_W
        _ll_resid_sd = np.std(residual, axis=0)
        _ll_resid_sd = np.maximum(_ll_resid_sd, 1e-8)
        _ll_last_fit = nt

    x_now = (r[:, -1] - _ll_mu) / _ll_sd
    pred = x_now @ _ll_W
    z = pred / _ll_resid_sd

    # Remove the day's common component so this remains a relative-value book.
    z = z - np.median(z)
    z_scale = 1.4826 * np.median(np.abs(z - np.median(z)))
    if z_scale <= 1e-9:
        z_scale = np.std(z) + 1e-9
    z = np.clip(z / z_scale, -4.0, 4.0)

    caps = LIMITS.copy()
    caps[0] *= LL_ALGO_CAP_FRAC
    caps[1:] *= LL_STOCK_CAP_FRAC
    return risk_mult * caps * np.tanh(z / LL_TEMP)


def _mlr_target_dollars(prc_so_far, risk_mult):
    """Apply the offline full-sample predictive matrix; never fit at runtime."""
    if prc_so_far.shape[1] < MLR_MIN_HIST:
        return np.zeros(N_INST)

    lp = np.log(np.maximum(prc_so_far[1:, -MLR_SLOPE_LB:], EPS))
    slope_now = (lp[:, -1] - lp[:, 0]) / (MLR_SLOPE_LB - 1)
    xs_now = (slope_now - MLR_X_MU) / MLR_X_SD

    # Genuine next-day forecast: buy positive forecasts, sell negative ones.
    pred = MLR_Y_MU + xs_now @ MLR_W
    raw_z = pred / MLR_RESID_SD

    # Keep the sleeve market-neutral and limited to the strongest names.
    raw_z = raw_z - np.median(raw_z)
    cs_scale = 1.4826 * np.median(np.abs(raw_z - np.median(raw_z)))
    if cs_scale <= 1e-9:
        cs_scale = np.std(raw_z) + 1e-9
    z = np.clip(raw_z / cs_scale, -4.0, 4.0)

    if MLR_TOP_K < z.size:
        keep = np.argpartition(np.abs(z), -MLR_TOP_K)[-MLR_TOP_K:]
        mask = np.zeros(z.size, dtype=bool)
        mask[keep] = True
        z = np.where(mask, z, 0.0)

    target = np.zeros(N_INST)
    target[1:] = (
        risk_mult
        * MLR_CAP_FRAC
        * LIMITS[1:]
        * np.tanh(z / MLR_TEMP)
    )
    return target

def getMyPosition(prcSoFar):
    global _prev_nt

    n_inst, nt = prcSoFar.shape
    if n_inst != N_INST or nt < 2:
        return np.zeros(n_inst, dtype=int)

    # The evaluator starts a new simulation by moving time backwards.
    if nt <= _prev_nt:
        reset_state()
    _prev_nt = nt

    log_prices = np.log(np.maximum(prcSoFar, EPS))
    vol_ratio = _market_vol_ratio(log_prices)
    ll_risk, mlr_risk = _risk_multipliers(vol_ratio)

    pair_book = _pair_target_dollars(log_prices)
    ll_book = _leadlag_target_dollars(prcSoFar, ll_risk)
    mlr_book = _mlr_target_dollars(prcSoFar, mlr_risk)

    target_dollars = pair_book + ll_book + mlr_book
    target_dollars = np.clip(target_dollars, -LIMITS, LIMITS)

    prices_now = np.maximum(prcSoFar[:, -1], EPS)
    return np.trunc(target_dollars / prices_now).astype(int)
